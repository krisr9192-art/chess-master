export type PuzzleDifficulty = 'rookie' | 'beginner' | 'intermediate' | 'advanced' | 'expert';

export const difficultyLevels: { value: PuzzleDifficulty; label: string; description: string; minRating: number; maxRating: number; color: string }[] = [
  { value: 'rookie', label: 'Rookie', description: 'Mate in 1, simple captures', minRating: 0, maxRating: 650, color: 'from-green-500 to-green-700' },
  { value: 'beginner', label: 'Beginner', description: 'Basic forks and pins', minRating: 651, maxRating: 800, color: 'from-blue-500 to-blue-700' },
  { value: 'intermediate', label: 'Intermediate', description: 'Mate in 2, combinations', minRating: 801, maxRating: 1000, color: 'from-yellow-500 to-yellow-700' },
  { value: 'advanced', label: 'Advanced', description: 'Complex tactics', minRating: 1001, maxRating: 1200, color: 'from-orange-500 to-orange-700' },
  { value: 'expert', label: 'Expert', description: 'Master level puzzles', minRating: 1201, maxRating: 2000, color: 'from-red-500 to-red-700' },
];

export function getPuzzlesByDifficulty(difficulty: PuzzleDifficulty): Puzzle[] {
  const level = difficultyLevels.find(l => l.value === difficulty);
  if (!level) return [];
  return puzzles.filter((p) => p.rating >= level.minRating && p.rating <= level.maxRating);
}

export function getRandomPuzzleByDifficulty(difficulty: PuzzleDifficulty, excludeIds: string[] = []): Puzzle | undefined {
  const available = getPuzzlesByDifficulty(difficulty).filter((p) => !excludeIds.includes(p.id));
  if (available.length === 0) return undefined;
  return available[Math.floor(Math.random() * available.length)];
}

export function getPuzzlesByRating(minRating: number, maxRating: number): Puzzle[] {
  return puzzles.filter((p) => p.rating >= minRating && p.rating <= maxRating);
}

export function getPuzzlesByTheme(theme: string): Puzzle[] {
  return puzzles.filter((p) => p.themes.includes(theme));
}

export function getRandomPuzzle(excludeIds: string[] = []): Puzzle | undefined {
  const available = puzzles.filter((p) => !excludeIds.includes(p.id));
  if (available.length === 0) return undefined;
  return available[Math.floor(Math.random() * available.length)];
}

export function getPuzzleById(id: string): Puzzle | undefined {
  return puzzles.find((p) => p.id === id);
}
